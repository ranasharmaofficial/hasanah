<h1>Practical Activities</h1>
<p><strong>&nbsp;</strong></p>
<p><strong>Detail of Practical activities</strong></p>
<p><strong>Under supervision:</strong> of Al-Hasanah Educational Trust, Araria, Bihar (India) <strong>1, Al-Hasanah Girls School</strong></p>
<p>( Primary, Middle, Secondary and Alimiah )</p>
<p>Al-Hasanah Girls School has been completed for female learners of the prophetic sciences in which female students from remote areas obtain the correct educations from the Qura&rsquo;n and the Sunnah and they take advantage of correct beliefs and methods of dawah to Allah.</p>
<h1>1-&nbsp;&nbsp;&nbsp; Department of memorizing the Holy Qaran</h1>
<p>The student memorizes the words of Allah (the Noble Qur&rsquo;an) with big efforts in this prestigious department.</p>
<p>In this department, 57 students memorize the words of Allah Almighty, and this year 22 students will graduate from it by permission of Allah.</p>
<h1>3 - Noor al-Huda Islamic School</h1>
<p>The Department of Memorizing the Noble Qur&rsquo;an, the Department of Primary education and Middle School</p>
<p>An Islamic school, <strong>Noor al-Huda</strong>, was built in Araria, Bihar (India) for learners of the Prophetic sciences in which male and female students obtain it from remote areas,&nbsp;they will receive the correct instructions from the Book and the Sunnah, and they will benefit from the study of the correct beliefs, and dawah method to Allah almighty.</p>
<h1>4-&nbsp;&nbsp; Sponsoring orphans and needy one from male and female students</h1>
<p>Al-Tawheed Association sponsors orphans and needy male and female students a full sponsorship of providing them textbooks and medical facilities, their expenses, housing provision, and other requirements.</p>
<h1>5-&nbsp;&nbsp; Take care of breakfast for the fasting people</h1>
<p>The Tawheed Association takes care of breaking the fast annually, and it sends breakfast to the poor family. It also implements the Iftar projects in the various mosques of the region, in which a large number of poor and needy fasting people break their fasts. In this project, it has also a great important role in terms of dawah to Allah by giving awareness lessons by the advocates / representatives of Tawheed Association.</p>
<p>We request the good people to participate to achieve and success of this project, so that we all can participate in <strong>Da`wah </strong>to Allah.</p>
<h1>6-&nbsp;&nbsp; Department of Da`wah and Guidance, especially for non-Muslims</h1>
<p>There is nothing hidden from anyone that the work of Dawah is very important, that the association coordinated the activities of advocacy field with the help of the preacher who visits villages and cities that have been covered by misguidance and heresy, and unbelief and atheism, so they do what they can, from their advocacy efforts to raise the word of Allah and spread the Salafi approach in the light of the Qur&rsquo;an and the correct Sunnah in a best possible way through the evidence and insight so that word of Allah can be supreme.</p>
<p>Praise be to Allah that the advocates of the association are acquainted with the requirements of this new era, using the means of modern advocacy.</p>
<h1>7-&nbsp;&nbsp; Well Drilling Project</h1>
<p>Public charitable matters have a great role in bringing in people's projects<strong>,</strong> Digging wells is one of the charitable projects, Therefore, the association was keen to provide water for those who have difficulty to obtain it, so that this would be purely for them in terms of dawah to Allah Almighty and it decorates them with the teaching of Islam and way of Islam, so the association implemented a lot of water pipe drillings in various regions of India. Therefore we urge good people to participate in this great project so that they may become among those who contributed to the Dawah to Allah through this project, and may Allah reward you with goodness.</p>
<h1>8-&nbsp;&nbsp; Providing meat of sacrifice (Qurbani) to the poor and needy people</h1>
<p>Keeping an eye on the needs of people and dawah The Tawheed Association has been taking care of the sacrifice project, where large numbers of sacrifices are slaughtered, and the meats of the sacrifice are distributed among poor and needy families to share the joy and happiness with Muslims and to make them feel that the Muslims are one integrated nation, loving and sympathetic.</p>
<h1>9-&nbsp;&nbsp; Construction of mosques in needy areas</h1>
<p>Mosques have a prominent role in spreading the creed, the method of the Qur'an and the Sunnah, because of this reason, building mosques in the various regions of India has become one of the most important goals of the Tawheed Association.</p>
<p>The Tawheed Association makes building, constructing, and renovating mosques in villages and cities so that the scope of the da&rsquo;wah can be in a wider range.</p>
<p>And thanks to Allah, then thanks to the good people of contributors and benefactors.</p>
<p>Therefore, we urge good people to contribute to the accomplishment of such projects.</p>
<h1>10 - Islamic library</h1>
<p>There is a central library Under the Al-Tawheed association from which male and female students and many young visitors visit the library to get benefit from it, and to obtain the sciences of the Qur&rsquo;an and the Sunnah, and reform their beliefs so that they try to apply while learning practically.</p>
<p>They try to apply with them to learn practically from the rulings and sciences of Sharia-based on the Qur&rsquo;an and the Sunnah.</p>
